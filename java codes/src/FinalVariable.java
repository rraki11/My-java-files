public class FinalVariable {
    public static void main (String[] args) {

        //A variable is called as final variable when it is declared by final keyword..

        final float PI = 3.14f;
        System.out.println(PI);

        //once a variable is declared by "final" keyword the value of that variable cannot be changed agian. Also its a good practice to Declare the variable uisng all CAPS for good practice.
    }
}
